def calc_wealth(bazi, ri):
    parts = bazi.split()
    total=0; items=[]
    for n,g,b in [('年干',parts[0][0],8),('月干',parts[1][0],12),('时干',parts[3][0],12)]:
        s=SS[ri][g]
        if s in CAIXING: total+=b; items.append(f"天干{g}={s}{b}分")
    for n,z,b in [('年支',parts[0][1],4),('月令',parts[1][1],40),('日支',parts[2][1],12),('时支',parts[3][1],12)]:
        for cg,pct in CANG[z]:
            s=SS[ri][cg]
            if s in CAIXING:
                sc=round(b*pct/100,1); total+=sc
                items.append(f"藏干{cg}({z})={s}{b}×{pct}%={sc}")
    return round(total,1), items

def run_engine(params):
    r = subprocess.run(['python3',ENGINE]+params.split()+['--json'], capture_output=True, text=True)
    m = re.search(r'\{.*\}', r.stdout, re.DOTALL)
    return json.loads(m.group()) if m else None

